<h1>Personuppgiftsbehandling</h1>

<p>De personuppgifter som vi samlar in kan komma att användas för att:</p>
<ul>
	<li>
		Skicka ut kallelse till årsmöte.
	</li>
	<li>
		Kontakta individuella medlemmar för att de:
		<ul>
			<li>
				Brutit mot någon <a href="/static/rules">lokalregel</a> eller sitt <a href="/Static/internet_rules">internetavtal</a>.
			</li>

			<li>
				Glömt saker i lokalen.
			</li>
		</ul>
	</li>
</ul>
<p>Personuppgifterna kan även komma att lämnas ut till bidragsgivare till föreningen med syfte att de ska kontrollera att föreningen inte ljuger om medlemsantal motsvarande.</p>
<p>De uppgifter som kan komma att lämnas ut är:</p>
<ul>
	<li>

		Personuppgifter (tex namn, telefonnummer, e-post, personnummer mm).
	</li>
	<li>
		När du blev medlem i föreningen mm.
	</li>
	<li>
		När du varit i föreningslokalen.
	</li>
</ul>
<p>Det enda sättet att komma åt dina uppgifter är att antingen du eller en administratör loggar in i PILS (bruse.proxxi.org) och går in och tittar på uppgifterna.</p>

