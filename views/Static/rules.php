<h1>Lokalregler</h1>
<ul>
	<li>
		För att vara i lokalen behöver man vara medlem i föreningen Proxxi.
		Att bli medlem kostar till och med det kalenderår du fyller 25 år 50 kr
		därefter kostar det 100 kr. Medlemskap gäller ett kalenderår. För att 
		bli medlem, prata med en <a href="/Group/view/2">nyckelbärare</a> i
		lokalen.
	</li>
	<li>
		<a href="/Attendance/create">Skriv upp dig</a> när du är i lokalen.
		Lokalen kan finnas för ett vi får bidrag från kommunen för att ungdomar
		är här.
	</li>
	<li>
		Plocka undan efter dig. Ingen tycker om att vara i en skräpig och
		stökig miljö.
	</li>
	<li>
		Ta det försiktigt med saker i lokalen. Soffor, stolar och datorer kan
		gå sönder.
	</li>
	<li>
		Försök att inte låta överdrivet mycket, det blir både en otrevlig miljö
		och kan leda till hörselskador.
	</li>
	<li>
		Stäng av datorn när ingen ska använda den så får vi mer pengar över till
		annat än el-kostnader.
	</li>
	<li>
		Alla former av tobak, alkohol och andra droger är strikt förbjudena i
		och i anslutning till lokalen. Ska du röka får du gå ut åtminstone till
		gatan.
	</li>
	<li>
		Var trevlig mot varandra och de boende i huset.
	</li>
</ul>
<p>
	Brott mot dessa regler kan få dig utkastad över dagen, portad en viss tid
	eller, i värsta fall, utesluten ur föreningen.
</p>

