<h1>Stadgar för Proxxi</h1>
<ul>
	<li>antagna 21 januari 2006</li>
	<li>ändrade 3 februari 2007</li>
	<li>ändrade 21 mars 2009</li>
</ul>
<ol>
	<li>
		<h3><a name="Förening och säte">Förening och säte</a></h3>
		<p>Proxxi är en ideell förening. Styrelsen har sitt säte i
		Stockholm. Föreningen är ansluten till
		<a href="http://www.sverok.se">Sveriges roll- och konfliktspelsförbund</a>.</p>
	</li>
	<li>
		<h3><a name="Syfte">Syfte</a></h3>
		<p>Föreningens syfte är främst att främja spelande av olika
		former av spel, sekundärt ska datorintresse stimuleras.
		Verksamheten ska vara drogfri och alkoholfri.</p>
	</li>
	<li>
		<h3><a name="Oberoende">Oberoende</a></h3>
		<p>Föreningen är religiöst och partipolitiskt obunden.</p>
	</li>
	<li>
		<h3><a name="Verksamhetsår">Verksamhetsår</a></h3>
		<p>Verksamhetsåret är 1 januari till 31 december.</p>
	</li>
	<li>
		<h3><a name="Medlemmar">Medlemmar</a></h3>
		<p>Som medlem antas intresserad som godkänner dessa stadgar och
		betalar medlemsavgift till föreningen. Avgiftens storlek
		beslutas på årsmötet. En medlem som allvarligt skadar föreningen
		kan avstängas av styrelsen. Avstängd medlem måste diskuteras på
		nästa årsmöte. Antingen så upphävs då avstängningen eller så
		utesluts medlemmen.</p>
	</li>
	<li>
		<h3><a name="Styrelsen och dess verksamhet">Styrelsen och dess verksamhet</a></h3>
		<p>Styrelsen ansvarar för föreningens medlemslista.
		bidragsansökningar, medlemsvärvning, att verkställa
		beslut som tas på årsmöten och övrig verksamhet. Styrelsen ska
		verka för att information ska nå så många medlemmar som möjligt.
		Styrelsen ska verka för att uppfylla föreningens jämställdhetspolicy.
		Föreningens styrelse består av ordförande, kassör och en eller flera
		ledamöter. Samma person får inte ha flera poster i styrelsen.
		Styrelsen väljs på årsmötet eller med den metod som årsmötet
		väljer, och tillträder direkt efter valet. Valbar är medlem i
		föreningen. Vid behov kan suppleanter väljas till styrelsen,
		dessa är då allmänna suppleanter. Styrelsen är beslutsmässig
		när hela styrelsen är kallad minst 2 dygn i förväg och mer än
		en tredjedel av ledamöterna är närvarande.</p>
	</li>
	<li>
		<h3><a name="Revisorer">Revisorer</a></h3>
		<p>För granskning av föreningens räkenskaper och förvaltning
		väljs på årsmötet två revisorer samt en suppleant. Valbar är
		person som inte sitter i styrelsen. Revisorerna och suppleanten
		behöver inte vara medlemmar i föreningen.</p>
	</li>
	<li>
		<h3><a name="Valberedning">Valberedning</a></h3>
		<p>För att ta fram förslag på personer till de i stadgarna
		föreskrivna valen väljs på årsmötet en valberedare. Valbar är
		medlem i föreningen.</p>
	</li>
	<li>
		<h3><a name="Ordinarie årsmöte">Ordinarie årsmöte</a></h3>
		<p>Ordinarie årsmöte hålls senast den 31 mars varje år.
		Styrelsen beslutar om tid och plats. För att vara behörigt måste
		medlemmarna meddelas minst två veckor i förväg. Om möjligt skall
		styrelsen nyttja email, anslag på hemsidan samt anslag i
		lokalen. Motioner skall inkomma tidigast en månad efter senaste
		årsmöte och senast den sista november. Styrelsen ska lämna
		bekräftelse på att de mottagit motionen. Motionerna ska gå med
		kallelsen till årsmötet. Följande ärenden ska alltid behandlas
		på ordinarie årsmöte:</p>
		<ol>
			<li>Mötets öppnande</li>
			<li>Mötets behörighet</li>
			<li>Val av mötets ordförande</li>
			<li>Val av mötets sekreterare</li>
			<li>Fastställande av dagordning</li>
			<li>Val av två justerare tillika rösträknare</li>
			<li>Styrelsens verksamhetsberättelse för förra året</li>
			<li>Ekonomisk berättelse för förra året</li>
			<li>Revisorernas berättelse för förra året</li>
			<li>
				Styrelsens redovisning av plan om hur likvida medel ska 
				räcka för fasta utgifter fram till nästa ordinarie 
				årsmöte
			</li>
			<li>Frågan om ansvarsfrihet för förra årets styrelse</li>
			<li>Behandlande av inkomna motioner</li>
			<li>
				Diskussionspunkt om verksamhetsplanens innehåll för att
				underlätta bedömning av vilka som passar bäst in i
				styrelsen
			</li>
			<li>Val av årets styrelse</li>
			<li>Val av årets revisor</li>
			<li>Val av årets valberedare</li>
			<li>Val av årets jämställdhetsrepresentanter</li>
			<li>Årets verksamhetsplan</li>
			<li>Årets budget samt fastställande av medlemsavgift</li>
			<li>Övriga frågor</li>
			<li>Mötets avslutande</li>
		</ol>
	</li>
	<li>
		<h3><a name="Extra årsmöte">Extra årsmöte</a></h3>
		<p>Om styrelsen eller revisorerna eller minst hälften av
		medlemmarna eller minst 10% av de röstberättigade (dock minst 42
		personer) kräver det kan styrelse, revisor eller röstberättigad
		kalla till extra årsmöte. På ett extra årsmöte kan bara de
		ärenden som nämnts i kallelsen behandlas. Extra årsmöte utlyses
		på samma sätt som ordinarie årsmöte.</p>
	</li>
	<li>
		<h3><a name="Firmateckning">Firmateckning</a></h3>
		<p>Föreningens firma tecknas av ordförande och kassör var för
		sig. Om särskilda skäl föreligger kan årsmötet utse annan person
		till firmatecknare. Om någon eller båda av firmatecknarna blir
		otillgängliga under en längre tid eller avsäger sig sitt uppdrag
		kan styrelsen utse interimma firmatecknare i väntan på extra
		årsmöte."</p>
	</li>
	<li>
		<h3><a name="Rösträtt">Rösträtt</a></h3>
		<p>Endast fullt betalande närvarande medlemmar äger rösträtt på
		årsmöte. På ordinarie årsmöte har även fullt betalande närvarande
		medlemmar från föregående år rösträtt. På styrelsemöte äger endast
		närvarande ur styrelsen rösträtt.</p>
	</li>
	<li>
		<h3><a name="Röstetal">Röstetal</a></h3>
		<p>Alla frågor som behandlas på årsmöte, extra årsmöte eller
		styrelsemöte avgörs med enkel röstövervikt om inget annat står i
		stadgarna. Nedlagda röster räknas ej. Varje person med rösträtt
		har en röst. Vid lika röstetal har föreningens ordförande
		utslagsröst. Övriga frågor antas på årsmöte med två tredjedelar
		av antalet röster.</p>
	</li>
	<li>
		<h3><a name="Stadgeändring">Stadgeändring</a></h3>
		<p>Dessa stadgar kan bara ändras på årsmöten. För att vara
		giltig måste ändringen antas med två tredjedelar av antalet
		röster. Då stadgeändring ska ske måste förslaget delges
		medlemmarna i kallelsen till mötet. I annat fall måste ändringen
		antas enhälligt. Ändringen av föreningens stadga om 
		<a href="#Syfte">syfte (§2)</a>,
		<a href="#Stadgeändring">stadgeändringar (§14)</a>, och 
		<a href="#Upplösning">upplösning (§15)</a> kräver likalydande
		beslut på två på varandra följande årsmöten.</p>
	</li>
	<li>
		<h3><a name="Upplösning">Upplösning</a></h3>
		<p>Förslag om föreningens upplösning får framläggas endast på
		ordinarie årsmöte. Att upplösningen ska behandlas måste framgå
		av kallelsen. Föreningen kan inte upplösas så länge minst fem
		medlemmar vägrar godkänna upplösningen. Vid upplösning ska
		föreningens skulder betalas. Därefter ska föreningens tillgångar
		gå till verksamhet i enlighet med föreningens syfte. Hur detta
		ska ske beslutas på sista årsmötet.</p>
	</li>
	<li>
		<h3><a name="Jämställdhetspolicy">Jämställdhetspolicy</a></h3>
		<p>Föreningen ska ha en jämställdhetspolicy som vägleder och
		inspirerar verksamhet samt styrelsearbete.</p>
	</li>
</ol>
